#include <iostream>
#include <cmath>
using namespace std;

int main() {

    cout << "a" << "   " << "b" << "   " << "Pow(a,b)\n";
    cout << 1 << "   " << 2 << "   " << pow(1,2)<< "\n";
    cout << 2 << "   " << 3 << "   " << pow(2,3)<< "\n";
    cout << 3 << "   " << 4 << "   " << pow(3,4)<< "\n";
    cout << 4 << "   " << 5 << "   " << pow(4,5)<< "\n";
    cout << 5 << "   " << 6 << "   " << pow(5,6)<< endl;
    return 0;
}